'''Use a dictionary to store information about a person you know.Store their first name,
last name, age, and the city in which they live.

You should have keys such as first_name, last_name, age, and city. Print each piece of
information stored in your dictionary.'''

person_info = {
    'first_name' : 'Abby',
    'last_name'  : 'Ace',
    'age' : 19,
    'city' : 'Sharjah'
}                            #dictionary contains all the information

print (f'First name: {person_info['first_name']}')  #prints out the first name
print (f'Last name: {person_info['last_name']}')    #prints out the last name
print (f'Age: {person_info['age']}')                #prints out the age
print (f'City: {person_info['city']}')              #prints out the city